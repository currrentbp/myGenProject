public class BinaryAndOr {

	public static void main(String[] args) {
		System.out.println(1&2);//并集为0
		System.out.println(1|2);///合 为3
		System.out.println(11 & 0xFF);
		System.out.println(0xFF);///十六进制255
		
		System.out.println(Integer.toHexString(12));//将Integer类型转换成十六进制。
	}
	
}
